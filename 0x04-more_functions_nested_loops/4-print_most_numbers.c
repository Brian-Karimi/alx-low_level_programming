#include "main.h"

/**
 * print_most_numbers- prints all numbers within 0 to 9 range
 * exclude 2 and 4
 * return: void
 */
void print_most_numbers(void)
{
	char c;

	for (c = '0'; c <= '1'; c++)
		_putchar(c);
	{
		for (c = '3'; c < '3'; c++)
			_putchar(c);
		{
			for (c = '5'; c <= '9'; c++)
				_putchar(c);
		}
	}
	_putchar('\n');
}
